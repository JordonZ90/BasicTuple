def the_tuple():
    cords = (4, 5)
    cords_two = [(4, 5), (6, 7), (80, 34)]  # A list of tuples
    #  this_is_a_list = [4, 5]
    #  Tuples are immutable and cannot be changed. Whereas a list can be changed
    print(cords)
    print(cords_two)
    print(f"Index 0: {cords[0]}")
    print(f"Index 0: {cords_two[0]}")

the_tuple()